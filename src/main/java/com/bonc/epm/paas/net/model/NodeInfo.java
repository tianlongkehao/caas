package com.bonc.epm.paas.net.model;

public class NodeInfo {
	private String name;
	private String ip;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMountPath() {
		return ip;
	}
	public void setMountPath(String mountPath) {
		this.ip = mountPath;
	}

}
